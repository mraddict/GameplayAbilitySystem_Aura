bool UAuraAttributeSet::SetEffectProperties(const FGameplayEffectModCallbackData& Data, FEffectProperties& Props) const
{
	// Source = causer of the effect
	// Target = target of the effect (owner of this AttributeSet)

	Props.EffectContextHandle = Data.EffectSpec.GetContext();
	Props.SourceASC = Props.EffectContextHandle.GetOriginalInstigatorAbilitySystemComponent();

	if (Props.SourceASC->AbilityActorInfo.IsValid() == false)
	{
		return false;
	}

	const FGameplayAbilityActorInfo* SourceAbilityActorInfo = Props.SourceASC->AbilityActorInfo.Get();

	if (SourceAbilityActorInfo->AvatarActor.IsValid() == false)
	{
		return false;
	}

	/** The physical representation of the owner, used for targeting and animation. This will often be null! */
	Props.SourceAvatarActor = SourceAbilityActorInfo->AvatarActor.Get();
	/** PlayerController associated with the owning actor. This will often be null! */
	Props.SourceController = SourceAbilityActorInfo->PlayerController.Get();

	if ((Props.SourceAvatarActor == nullptr) && (Props.SourceController == nullptr))
	{
		if (const APawn* Pawn = Cast<APawn>(Props.SourceAvatarActor))
		{
			Props.SourceController = Pawn->GetController();
		}

		return false;
	}

	Props.SourceCharacter = Cast<ACharacter>(Props.SourceController->GetPawn());

	if (Data.Target.AbilityActorInfo.IsValid() == false)
	{
		return false;
	}

	const FGameplayAbilityActorInfo* TargetAbilityActorInfo = Data.Target.AbilityActorInfo.Get();

	if (TargetAbilityActorInfo->AvatarActor.IsValid() == false)
	{
		return false;
	}

	Props.TargetAvatarActor = TargetAbilityActorInfo->AvatarActor.Get();
	Props.TargetController = TargetAbilityActorInfo->PlayerController.Get();
	Props.TargetCharacter = Cast<ACharacter>(Props.TargetAvatarActor);
	Props.TargetASC = UAbilitySystemBlueprintLibrary::GetAbilitySystemComponent(Props.TargetAvatarActor);

	return true;
}